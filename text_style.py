import re

def humanize_outgoing_text(text):
    text = str(text or "").strip()
    if not text:
        return text

    # Сначала точечные замены, пока длинное тире ещё на месте
    replacements = {
        'Готов оперативно подключиться к решению технических задач, связанных с API и взаимодействием с клиентами': 'Готов обсудить технические задачи, API и интеграции',
        'Год назад участвовал в проекте в Bizon VR': 'Есть около года практического опыта в Bizon VR',
        'интеграциями и ботами. Год назад участвовал': 'интеграциями и ботами. Есть около года практического опыта',
        'где внедрял решения для CRM и ботами': 'где занимался CRM-решениями и ботами',
        'Удаленка подходит. Офис или гибрид подходят только в Екатеринбурге.': 'Готов обсудить задачи и формат работы.',
        'Удалёнка подходит. Офис или гибрид подходят только в Екатеринбурге.': 'Готов обсудить задачи и формат работы.',
        'Удаленка подходит, офис или гибрид, только в Екатеринбурге': 'Готов обсудить задачи и формат работы',
        'Удалёнка подходит, офис или гибрид, только в Екатеринбурге': 'Готов обсудить задачи и формат работы',
        'Опыт работы с REST API и backend-разработкой есть': 'Есть опыт работы с интеграциями и backend-задачами',
        'Готов обсудить вакансию в комфортном для вас формате': 'Готов обсудить вакансию и задачи',
        'и ботов ': 'и ботами ',
        'и ботов.': 'и ботами.',
        'интеграциями, ботов': 'интеграциями и ботами',
        'интеграциями и ботов': 'интеграциями и ботами',
        "Мой основной стек —": "Мой основной стек:",
        "Мой основной стек -": "Мой основной стек:",
        "Думаю, мой опыт может быть полезен вашей команде": "Буду рад быть полезен в задачах",
        "Думаю, мой опыт может быть полезен": "Буду рад быть полезен",
        "как мой опыт может быть полезен вашей команде": "чем могу быть полезен в задачах",
        "Готов быстро выйти на связь и обсудить детали.": "Буду рад обсудить детали",
        "Готов быстро выйти на связь и обсудить детали": "Буду рад обсудить детали",
        "Готов быстро выйти на связь": "Буду рад выйти на связь",
        "Буду рад пообщаться": "Буду рад обсудить",
        "в зависимости от задач, формата работы и условий": "в зависимости от задач и условий",
        "на данный момент": "сейчас",
        "в рамках": "в",
        "осуществлял": "занимался",
        "имеется опыт": "есть опыт",
        "коммуникации": "общения",
    }

    for old, new in replacements.items():
        text = text.replace(old, new)

    # Потом убираем длинные тире в остальных местах
    text = text.replace(" — ", ", ")
    text = text.replace("—", "-")
    text = text.replace("–", "-")
    text = text.replace("−", "-")

    # Если после замен всё равно получилось "стек, Python", исправляем на двоеточие
    text = text.replace("Мой основной стек, Python", "Мой основной стек: Python")

    # Убираем канцелярские хвосты
    text = re.sub(r"\s+;", ",", text)
    text = re.sub(r";\s+", ", ", text)
    text = re.sub(r"[ \t]+", " ", text)

    lines = [line.strip() for line in text.splitlines()]
    text = "\n".join(lines)
    text = re.sub(r"\n{3,}", "\n\n", text)

    # Убираем точку в самом конце сообщения
    if text.endswith(".") and not text.lower().endswith(("т.д.", "т.п.", "и т.д.", "и т.п.")):
        text = text[:-1]

    return text.strip()
