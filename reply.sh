#!/usr/bin/env bash
set -euo pipefail

cd /opt/hh-bot
source /opt/hh-bot/venv/bin/activate

LOG_DIR="/opt/hh-bot/logs"
STATE_DIR="/opt/hh-bot/state"
FACTS_FILE="/opt/hh-bot/candidate_facts.txt"

mkdir -p "$LOG_DIR" "$STATE_DIR"

TODAY="$(date '+%F')"
LOG_FILE="$LOG_DIR/reply-$TODAY.log"
LOCK_FILE="$STATE_DIR/reply.lock"

exec 9>"$LOCK_FILE"
flock -n 9 || {
  echo "[$(date '+%F %T')] Reply already running, exit" >> "$LOG_FILE"
  exit 0
}

if [ "${NO_RANDOM_SLEEP:-0}" = "1" ]; then
  echo "[$(date '+%F %T')] NO_RANDOM_SLEEP=1, skip random delay" >> "$LOG_FILE"
else
  DELAY=$((RANDOM % 900))
  echo "[$(date '+%F %T')] Random reply delay: ${DELAY}s" >> "$LOG_FILE"
  sleep "$DELAY"
fi

FACTS="$(cat "$FACTS_FILE")"

REPLY_AI_SYSTEM="Ты отвечаешь работодателям в чатах HeadHunter от имени Ярослава. Ты кандидат, не работодатель. Пиши кратко, спокойно, профессионально, только на русском. Не используй Markdown, списки, подпись и длинные объяснения.

Главное правило: нельзя выдумывать факты. Используй только подтверждённую информацию ниже.

${FACTS}

Если вопрос работодателя касается опыта, которого нет в подтверждённых фактах, не приписывай его.

Если последнее сообщение работодателя является завершением анкеты, благодарностью, автоитогом, фразой вроде «представитель работодателя ознакомится», «благодарю за ответы», «мы свяжемся», «если подойдёте, продолжим обсуждение» — ответь строго одним словом: __SKIP__

Если сообщение не требует ответа, безопасного честного ответа нет или вакансия явно неподходящая — ответь строго одним словом: __SKIP__

Никогда не пиши «спасибо за отклик на вакансию», потому что это фраза работодателя, а не кандидата."

REPLY_PROMPT='Ответь работодателю в чате HeadHunter от лица кандидата Ярослава. Не выдумывай опыт. Если можно честно ответить на основе подтверждённых фактов — ответь кратко. Если вопрос про неподтверждённый опыт, но можно честно сказать что основного коммерческого опыта нет и кандидат готов разобраться — так и ответь. Если работодатель просит заполнить анкету, пройти тестирование, пройти интервью в Telegram, перейти по ссылке, зарегистрироваться, написать на почту, отправить документы, назвать телефон, выбрать время или выполнить внешнее действие — верни строго только одну строку: __ASK__: и конкретный вопрос для Ярослава по сути просьбы работодателя. Если обсуждаются обучение, смены, график, ночные смены, доплата, зарплата, дата выхода, личная готовность, курс, регистрация или любые условия, где нужно решение кандидата — верни строго только одну строку: __ASK__: и конкретный вопрос для Ярослава. В __ASK__ нельзя писать общие фразы: '"'"'что нужно уточнить у Ярослава'"'"', '"'"'что спросить у Ярослава'"'"', '"'"'нужно уточнение'"'"'. Всегда формулируй конкретно: например, готов ли он пройти тест, готов ли регистрироваться, подходит ли формат, есть ли опыт конкретной технологии. Если возвращаешь __ASK__, не добавляй перед ним и после него никакой ответ работодателю. Если вакансия или вопрос про явно запрещённые технологии/направления — верни только __SKIP__. Если последнее сообщение не требует ответа — верни только __SKIP__. Не обещай внешние действия. Не пиши, что у кандидата нет дополнительных вопросов, если это не было явно сказано кандидатом.'

{
  echo "============================================================"
  echo "START REPLY: $(date '+%F %T')"
  echo "============================================================"
} >> "$LOG_FILE"

set +e

hh-applicant-tool reply-employers \
  --ai \
  --ai-system "$REPLY_AI_SYSTEM" \
  --prompt "$REPLY_PROMPT" \
  --max-pages "${HH_REPLY_MAX_PAGES:-5}" \
  "$@" 2>&1 | tee -a "$LOG_FILE"

STATUS="${PIPESTATUS[0]}"
set -e

{
  echo "STATUS: $STATUS"
  echo "END REPLY: $(date '+%F %T')"
  echo
} >> "$LOG_FILE"

exit "$STATUS"
